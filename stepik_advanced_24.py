n = int(input())
matrix = []
for _ in range(n):
    matrix.append([int(num) for num in input().split()])
for i in range(n):
    for j in range(n // 2):
        matrix[j][i], matrix[-j - 1][i] = matrix[-j - 1][i], matrix[j][i]
for r in range(n):
    for c in range(n):
        print(matrix[r][c], end=" ")
    print()
