n = int(input())

matrix = []
for _ in range(n):
    matrix.append([0 for _ in range(n)])

for r in range(n):
    for c in range(n):
        if r + c + 1 == n:
            matrix[r][c] = 1
        elif r < c and r > n - 1 - c:
            matrix[r][c] = 2
        elif r > c and r > n - 1 - c:
            matrix[r][c] = 2
        elif r >= n // 2 and c >= n // 2:
            matrix[r][c] = 2
for r in range(n):
    for c in range(n):
        print(matrix[r][c], end=' ')
    print()
