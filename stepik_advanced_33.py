n = int(input())
matrix = []

for r in range(n):
    matrix.append([0 for _ in range(n)])

for r in range(n):
    for c in range(n):
        if r == c or c == n - r - 1:
            matrix[r][c] = 1
        elif r < c and r < n - 1 - c:
            matrix[r][c] = 1
        elif r > c and r > n - 1 - c:
            matrix[r][c] = 1

for r in range(n):
    for c in range(n):
        print(str(matrix[r][c]).ljust(3), end=' ')
    print()
