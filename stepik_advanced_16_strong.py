input_string_list = input().split()
length = int(input())


def chunked(input_list, input_length):
    output_list = [[]]
    if len(input_list) > 1:
        index_out = 0

        for i in range(1, len(input_list) + 1):
            if i % input_length != 0:
                output_list[index_out].append(input_list[i - 1])
            else:
                output_list[index_out].append(input_list[i - 1])
                if i != len(input_list):
                    output_list.append([])
                    index_out += 1
        return output_list
    return []


print(chunked(input_string_list, length))
