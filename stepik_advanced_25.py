n = int(input())
matrix = []
for _ in range(n):
    matrix.append([int(num) for num in input().split()])

matrix_output = []
for _ in range(n):
    matrix_output.append([0 for i in range(n)])

for i in range(n):
    for j in range(n):
        matrix_output[i][j] = matrix[-j - 1][i]

for r in range(n):
    for c in range(n):
        print(matrix_output[r][c], end=' ')
    print()
