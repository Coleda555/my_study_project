def who_will_win(n, k):
    soldiers = [i for i in range(1, n + 1)]
    counter = 0
    while soldiers.count(0) < len(soldiers) - 1:
        for i in range(len(soldiers)):
            if soldiers.count(0) >= len(soldiers) - 1:
                break
            if soldiers[i] != 0:
                counter += 1
                if counter // k > 0 and counter % k == 0:
                    soldiers[i] = 0
    for win in soldiers:
        if win != 0:
            print(win)
            break

who_will_win(2, 1)  # 2
