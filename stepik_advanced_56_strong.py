def merge(values):
    set_keys = set()
    for item in values:
        for key in item:
            set_keys.add(key)
    result_output = {}
    for key in set_keys:
        result_output[key] = set()
    for key in set_keys:
        for item in values:
            if key in item:
                result_output[key].add(item[key])
    return result_output


