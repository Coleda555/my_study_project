size = input().split()
n, m = int(size[0]), int(size[1])
matrix = []
for _ in range(n):
    matrix.append([0 for _ in range(m)])
step = 0
value = 1
while step != m * n + 1:
    for i in range(n):
        for j in range(m):
            if i + j == step:
                matrix[i][j] = value
                value += 1
    step += 1

for r in range(n):
    for c in range(m):
        print(str(matrix[r][c]).ljust(3), end=' ')
    print()
