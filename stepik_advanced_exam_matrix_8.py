n = int(input())
matrix = []
for _ in range(n):
    matrix.append([0 for _ in range(n)])
counter = 0
while counter < n:
    for i in range(n):
        for j in range(n):
            if i - counter == j:
                matrix[i][j] = counter
            elif j - counter == i:
                matrix[i][j] = counter
    counter += 1

for r in range(n):
    for c in range(n):
        print(matrix[r][c], end=' ')
    print()