n = int(input())
matrix = []
for _ in range(n):
    matrix.append([int(num) for num in input().split()])
numbers = []
for i in range(n):
    for j in range(n):
        if i <= j and i >= n - 1 -j:
            numbers.append(matrix[i][j])
        elif i >= j and i >= n - 1 -j:
            numbers.append(matrix[i][j])

print(max(numbers))